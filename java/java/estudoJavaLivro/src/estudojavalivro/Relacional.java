public class Relacional {
    public static void main(String args[]){
        int a = 15;
        int b = 12;
        System.out.printf("Valores: a = %d, b = %d%n", a, b);
        System.out.printf("Valores: a == b --> %b%n", (a == b));
        System.out.printf("Valores: a != b --> %b%n", (a != b));
        System.out.printf("Valores: a < b --> %b%n", (a < b));
        System.out.printf("Valores: a > b --> %b%n", (a > b));
        System.out.printf("Valores: a <= b --> %b%n", (a <= b));
        System.out.printf("Valores: a >= b --> %b%n", (a >= b));       
    }
    
}
